<script type="text/javascript">
    var datocuriosoAnimado = false;
    var nosotrosAnimado = false;
    var contactoAnimado = false;
    var textoEmpresaAnimado = false;
    function movil(){
        if(/android|webos|iphone|ipad|ipod|blackberry|iemobile|opera mini/i.test(navigator.userAgent.toLowerCase())) return true;
        return false;
    }

    function animarNosotros(){
        if(nosotrosAnimado) return;
        $(".trianguloArriba").addClass("trianguloArriba-abierto");
        $(".trianguloAbajo").addClass("trianguloAbajo-abierto");
        $(".texto-nosotros h3").addClass("texto-nosotros-titulo");
        $(".texto-nosotros p").addClass("texto-nosotros-cuerpo");
        $(".logo-nosotros").addClass("logo-nosotros-show");
        nosotrosAnimado = true;
    }

    function animarTextoEmpresa(){
        if(textoEmpresaAnimado) return;
        mostrar("mision");
        $(".texto-empresa").addClass("texto-empresa-abierto");
        $(".inicio-mision").addClass("animado-mision");
        $(".inicio-vision").addClass("animado-vision");
        $(".inicio-filosofia").addClass("animado-filosofia");
        setTimeout(function(){
            $(".animado-mision").removeClass("inicio-mision");
            $(".animado-vision").removeClass("inicio-vision");
            $(".animado-filosofia").removeClass("inicio-filosofia");
        },2000);
        textoEmpresaAnimado = true;
    }

    function animarContacto(){
        if(contactoAnimado) return;
        $(".cuadroContacto").addClass("cuadroContacto-listo");
        contactoAnimado = true;
    }

    function animarDatoCurioso(){
        if(datocuriosoAnimado) return;
        $(".textoCurioso").addClass("textoCurioso-listo");
        datocuriosoAnimado = true;
    }

    var menuChico = false;

    function waypointsLoad(){
        var sections = $("section"); //obtenemos las secciones
        sections.waypoint({
            handler: function (event, direction) { //verificamos que seccion esta seleccionada y si va hacia arriba o hacia abajo para decidir cual debe de ser el seleccionado
                var active_section = $(this);
                if(direction === 'up'){
                    console.log('arriba');
                    if(active_section.prev().attr('id') == "home"){
                        $(".logo-menu").removeClass("logo-menu-mostrar");
                        $(".submenu").removeClass("submenu-chico");
                        $(".menu").removeClass("menu-chico");
                        menuChico = false;
                        return;
                    }
                }
                if(active_section.attr("id") == "datocurioso"){
                    animarDatoCurioso();
                }

                if(active_section.attr("id") == "nosotros"){
                    if(!menuChico){
                        $(".logo-menu").addClass("logo-menu-mostrar");
                        $(".submenu").addClass("submenu-chico");
                        $(".menu").addClass("menu-chico");
                        menuChico = true;
                    }
                    animarNosotros();
                }

                if(active_section.attr("id") == "mision"){
                    animarTextoEmpresa();
                }

                if(active_section.attr("id") == "servicios"){
                    $(".texto-servicios").addClass("animaServicios");
                }

                if(active_section.attr("id") == "contacto"){
                    animarContacto();
                }
            },
            offset: '5%'
        });
    }

    $(document).ready(function() {
 
  $("#owl-demo").owlCarousel({
        items : 3,
        lazyLoad : true,
        navigation : false
      }); 
     
    });

    $(document).ready(function(){
        if(movil()){
            $("#home").css('background-attachment','local');
            $("#datocurioso").css('background-attachment','local');
            $("#datocurioso2").css('background-attachment','local');
            $("#nosotros").css('background-attachment','local');
            $("#contacto").css('background-attachment','local');
            $("#servicios").css('background-attachment','local');
            $("#rutas").css('background-attachment','local');
            $("#mision").css('background-attachment','local');
            $("#infocontacto").css('background-attachment','local');
        }
        waypointsLoad();
    });

    $(".boton-menu-movil").click(function(){
        $(".cuerpo-menu-movil").addClass("menu-movil-abierto");
    });

    $(".cerrar-menu-movil").click(function(){
        $(".cuerpo-menu-movil").removeClass("menu-movil-abierto");
    })

    function esconder(str) {
        $(".texto-"+str).fadeOut('');
        $(".animado-"+str).removeClass('texto-activo');
    }

    function mostrar(str){
        $(".texto-"+str).fadeIn('');
        $(".animado-"+str).addClass('texto-activo');
    }

    $(function () {
            $(".boton-menu").click(function (event) {
                $(".cuerpo-menu-movil").removeClass("menu-movil-abierto");
                $.scrollTo(
                           "#" + $(this).attr("data-id"),
                           {
                               duration: 2000,
                               easing: 'easeInOutExpo',
                               axis: 'y',
                           }
                           );
                return false;

            });

        });

    $(".mision-botones-container > p").click(function() {
        var data = $(this).attr("data-boton");
        if(data == "mision"){
            esconder("filosofia");
            esconder("vision");
            mostrar("mision");
            return;
        }

        if(data == "vision"){
            esconder("mision");
            esconder("filosofia");
            mostrar("vision");
            return;
        }

        esconder("vision");
        esconder("mision");
        mostrar("filosofia");
    })
</script>